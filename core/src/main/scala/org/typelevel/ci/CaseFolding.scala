/** These are lookup tables for case folding. There are several different case
  * folding algorithms which can be employed with different trade offs.
  *
  * @note Some case folding, in particular full case folding, can yield more
  *       codePoints than the original value. That is, it can ''increase'' the
  *       size of `String` values once folded.
  *
  * @see [[https://www.unicode.org/versions/Unicode14.0.0/ch05.pdf#G21790 Caseless Matching]]
  * @see [[https://www.unicode.org/Public/UCD/latest/ucd/CaseFolding.txt Unicode Case Folding Tables]]
