    codePoint match {
      case 0x0049 => 0x0131 // LATIN CAPITAL LETTER I
      case 0x0130 => 0x0069 // LATIN CAPITAL LETTER I WITH DOT ABOVE
      case _ =>
        simpleCaseFoldedCodePoints(codePoint)
    }

